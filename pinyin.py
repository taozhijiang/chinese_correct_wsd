def word2pinyin(word):
    """ hanzi should be unicode string"""
    return "'".join([hanzi2pinyin(hz) for hz in word])

def word2pinyin_split(word,sp):
    """ hanzi should be unicode string"""
    return sp.join([hanzi2pinyin(hz) for hz in word])

